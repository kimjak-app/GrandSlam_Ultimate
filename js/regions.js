function _norm(s) {
  return String(s || '').trim();
}

// --- Provider functions (UI에서는 이 함수만 사용) ---
function getRegion1List(countryCode) {
  const cc = _norm(countryCode || 'KR');
  if (cc !== 'KR') return [];
  return Object.keys(_KR_REGIONS);
}

function getRegion2List(countryCode, region1) {
  const cc = _norm(countryCode || 'KR');
  const r1 = _norm(region1);
  if (cc !== 'KR') return [];
  return _KR_REGIONS[r1] || ['직접입력…'];
}

function buildRegionKey(countryCode, region1, region2) {
  const cc = _norm(countryCode || 'KR') || 'KR';
  const r1 = _norm(region1) || '미지정';
  const r2 = _norm(region2) || '미지정';
  return `${cc}|${r1}|${r2}`;
}

